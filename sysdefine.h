#ifndef SYSDEFINE_H
#define SYSDEFINE_H

/**
 * @file sysdefine.h
 * @brief 系统全局定义头文件
 * 
 * 定义了整个TCP图像传输项目中使用的关键参数
 * 这些参数决定了图像的规格和数据处理方式
 */

/**
 * @def WIDTH
 * @brief 图像宽度像素数
 * 
 * 定义接收图像的标准宽度为1024像素
 * 该值必须与发送端的图像宽度匹配，否则会导致图像显示异常
 */
#define WIDTH   640

/**
 * @def HEIGHT  
 * @brief 图像高度像素数
 * 
 * 定义接收图像的标准高度为1024像素
 * 该值必须与发送端的图像高度匹配，否则会导致图像显示异常
 */
#define HEIGHT  2048

/**
 * @def CHANLE
 * @brief 图像通道数
 * 
 * 定义图像的颜色通道数为2（默认值）
 * 注意：这里可能是拼写错误，应该是CHANNEL
 * 
 * 支持的通道数说明（8位深度）：
 * - 1：灰度图像（8bit Gray）
 * - 2：双通道图像（8bit Gray + Alpha 或其他）
 * - 3：RGB彩色图像（8bit RGB）
 * - 4：RGBA彩色图像（8bit RGBA）
 * - 5-8：多通道图像（8bit 多光谱/高光谱）
 * 
 * 系统最大支持：8bit 8通道
 * - 位深度：8位 (0-255)
 * - 最大通道数：8通道
 * - 内存限制：单图像最大50MB
 * 
 * 多通道图像显示：
 * - 1通道：直接显示灰度
 * - 3通道：显示为RGB彩色
 * - 4通道：显示为RGBA彩色
 * - 其他通道数：提取第一通道显示为灰度
 */
#define CHANLE 2

/**
 * @brief 图像数据总大小计算
 * 
 * 图像数据总字节数 = WIDTH × HEIGHT × CHANLE
 * 当前配置下：1024 × 1024 × 2 = 2,097,152 字节 ≈ 2MB
 * 
 * 修改以上任一参数时，需要确保：
 * 1. 发送端使用相同的参数配置
 * 2. 网络带宽足够传输相应大小的数据
 * 3. 系统内存足够分配相应大小的缓冲区
 */

#endif // SYSDEFINE_H
