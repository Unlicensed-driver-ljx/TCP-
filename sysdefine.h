#ifndef SYSDEFINE_H
#define SYSDEFINE_H

/**
 * @file sysdefine.h
 * @brief 系统全局定义头文件
 * 
 * 定义了整个TCP图像传输项目中使用的关键参数
 * 这些参数决定了图像的规格和数据处理方式
 */

/**
 * @def WIDTH
 * @brief 图像宽度像素数
 * 
 * 定义接收图像的标准宽度为1024像素
 * 该值必须与发送端的图像宽度匹配，否则会导致图像显示异常
 */
#define WIDTH   640

/**
 * @def HEIGHT  
 * @brief 图像高度像素数
 * 
 * 定义接收图像的标准高度为1024像素
 * 该值必须与发送端的图像高度匹配，否则会导致图像显示异常
 */
#define HEIGHT  2048

/**
 * @def CHANLE
 * @brief 图像通道数
 * 
 * 定义图像的颜色通道数为2
 * 注意：这里可能是拼写错误，应该是CHANNEL
 * 
 * 通道数说明：
 * - 1：灰度图像
 * - 2：通常用于灰度+Alpha通道，或特殊的双通道图像
 * - 3：RGB彩色图像  
 * - 4：RGBA彩色图像（包含透明度通道）
 * 
 * 当前设置为2，表示这是一个双通道图像格式
 * 如果要改为标准灰度图像，请将此值改为1
 * 如果要改为RGB彩色图像，请将此值改为3
 */
#define CHANLE 2

/**
 * @brief 图像数据总大小计算
 * 
 * 图像数据总字节数 = WIDTH × HEIGHT × CHANLE
 * 当前配置下：1024 × 1024 × 2 = 2,097,152 字节 ≈ 2MB
 * 
 * 修改以上任一参数时，需要确保：
 * 1. 发送端使用相同的参数配置
 * 2. 网络带宽足够传输相应大小的数据
 * 3. 系统内存足够分配相应大小的缓冲区
 */

#endif // SYSDEFINE_H
